    public void presentarClasificacion(List<ResultadoJugador> clasificacionJugadores) {
        
        this.setVisible(true);
        this.listaJugadores.setText(null);
        this.listaCondenas.setText(null);
        
        // Actualizamos la clasificacion
        for (ResultadoJugador resultadoJugador : clasificacionJugadores) {
            String nombreJugador = resultadoJugador.getJugador().getNombre();
            AcumuladoJugador resultado = clasificacion.get(nombreJugador); 
            if ( resultado != null) {
                int condena = resultado.getTotalCondena();
                resultado.setTotalCondena(condena + resultadoJugador.getTiempoCondena() );
                int partidas = resultado.getPartidasJugadas();
                resultado.setPartidasJugadas(partidas + 1);
                clasificacion.put(nombreJugador, resultado);
            } else {
                resultado = new AcumuladoJugador( resultadoJugador.getJugador() ,
                            resultadoJugador.getTiempoCondena(), 1);
                clasificacion.put(nombreJugador, resultado);
            }
        }
        // Presentamos la clasificacion
        List<AcumuladoJugador> presentarClasificacion;
        presentarClasificacion = new ArrayList(clasificacion.values());
        
        Collections.sort(presentarClasificacion);
        for (AcumuladoJugador resultadoJugador : presentarClasificacion) {
            listaJugadores.append(resultadoJugador.getJugador().getNombre() + 
                    " (" + resultadoJugador.getPartidasJugadas() + ")\n");
            listaCondenas.append(Integer.toString(resultadoJugador.getTotalCondena()) + "\n");
        }
        
    }

